class Example38{
	public static void main(String args[]){
		boolean x=true,y=true,z=false;
		System.out.println("x&&y:"+(x&&y));
		System.out.println("y&&z:"+(y&&z));
		System.out.println("x||y:"+(x||y));
		System.out.println("y||z:"+(y||z));
		System.out.println("!x:"+(!x));
		System.out.println("!y:"+(!y));
}
}